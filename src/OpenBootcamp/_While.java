package OpenBootcamp;

public class _While {
    public static void main(String[] args) {
        int numeroWhile = 0;
        while (numeroWhile < 3){

            System.out.println(numeroWhile);
            numeroWhile++;
        }

        System.out.println();
        System.out.println();
        System.out.println();

        int numeroDoWhile = 0;

        do {
            System.out.println(numeroDoWhile);
            numeroDoWhile++;
        }while ( numeroWhile>3);
    }
}
